package com.chongweima.stats;

public class AnalyzerConfigurationException extends java.lang.RuntimeException {

    public AnalyzerConfigurationException() {
    }

    public AnalyzerConfigurationException(String message) {
        super(message);
    }

    public AnalyzerConfigurationException(String message, Throwable cause) {
        super(message, cause);
    }

    public AnalyzerConfigurationException(Throwable cause) {
        super(cause);
    }
}
